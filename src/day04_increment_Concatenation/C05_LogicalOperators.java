package day04_increment_Concatenation;

public class C05_LogicalOperators {
    public static void main(String[] args) {
        int a=10;
        int b=15;
        int c=20;
        System.out.println(a<10 && b>20);
        System.out.println(c>a && b>=20);
        System.out.println(a*b>30 && b-a<20);
        System.out.println(a>30 || b<10);
        System.out.println(c>20 || b+a<30);
    }
}
