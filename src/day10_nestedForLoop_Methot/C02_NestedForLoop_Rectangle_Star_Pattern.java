package day10_nestedForLoop_Methot;

public class C02_NestedForLoop_Rectangle_Star_Pattern {
    public static void main(String[] args) {


        for (int i = 1; i <=4; i++) {
            for (int j = 1; j <=5 ; j++) {
                System.out.print("* ");
            }
            System.out.println("");
        }
    }
}
