package day11_method_Overloading_WhileLoop;

public class C09_WhileLoop_ForLoop {
    public static void main(String[] args) {
        for (int i = 1; i <=100; i++) {
            System.out.print(i+" ");

        }
        System.out.println("\n------------------------------------------------------");
        int number=1;
        while (number<=100){
            System.out.print(number+" ");
            number++;
        }
    }
}
