package day03_dataCasting_WrapperClasses;

public class C02_DataCasting {
    public static void main(String[] args) {
        int number1=10;
        int number2=100;
        int number3=130;
        int number4=520;

        byte byt=(byte)number1;
        System.out.println(byt);

        byte byt1=(byte)number2;
        System.out.println(byt1);

        byte byt2=(byte)number3;
        System.out.println(byt2);

        double dbl=number4;
        System.out.println(number3/number2);
        System.out.println(dbl/number3);
    }
}
